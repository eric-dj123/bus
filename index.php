                                            Royal Express operated a limited number of white or yellow (the secondhand school bus colors) Toyota Coaster already in 2012 and possibly earlier. It was one of the competitors for the new urban services in Kigali. It won zone 2 of the 2013 tender and started operating the 200-series routes on August 30, 2013, under a five year contract. The company colors are orange and white. The fleet initially consisted of Toyota Coasters and a few other midibuses. At first a mixed fleet of older vehicles was used. Beginning of 2014 some 10 secondhand Hyundai Aerotown were bought and more large buses were added in July 2014 when, in collaboration with KBS, a fleet of 15 Yutong ZK6108HGC was put into service.

                                            The transport system in Rwanda centres primarily around the road network. Paved roads lie between the capital, Kigali, and most other major cities and towns in the country. Rwanda is also linked by road with other countries in the African Great Lakes, via which the majority of the country's imports and exports are made.

The country has an international airport at Kigali, serving one domestic and several international destinations, and also has limited transport between the port cities on Lake Kivu.There are currently no railways in Rwanda.

A large amount of investments in the transport infrastructure has been made by the goverment Since the 1994 genocide, with aid from the European Union, China, Japan and others.

